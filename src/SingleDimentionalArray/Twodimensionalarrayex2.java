package SingleDimentionalArray;

public class Twodimensionalarrayex2 {

	public static void main(String[] args) {
		
		int a[][] ={{5,10,15},{25,30,35},{40,45,50},{55,60,65},{70,75,80}};
		// int a[] = new int[5][3]
		
		for(int i[]:a)
		{
			for(int j:i)
				System.out.print(j+"  ");
			System.out.println();
		}
		
	}
	

}
